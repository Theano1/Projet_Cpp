#include <cstdio>
#include "Case.h"
#include "Environnement.h"


// ===========================================================================================
//                                       CONSTRUCTORS
// ===========================================================================================

Case::Case(int x, int y, float A){
  x_ = x;
  y_ = y;
  empty_ = false;
  Aout_ = A;
  Bout_ = 0.;
}

// ===========================================================================================
//                                       DESTRUCTORS
// ===========================================================================================


Case::~Case() = default;

// ===========================================================================================
//                                         GETTERS
// ===========================================================================================

float Case::Aout(){
  return Aout_;
}


float Case::Bout(){
  return Bout_;
}



int Case::x(){
  return x_;
}


int Case::y(){
  return y_;
}



bool Case::empty(){
  return empty_;
}





// ===========================================================================================
//                                         SETTERS
// ===========================================================================================


void Case::set_empty(bool empt){
  empty_ = empt;
  }

void Case::set_Aout(float c){
  Aout_ = c;
}


void Case::set_Bout(float c){
  Bout_ = c;
}



